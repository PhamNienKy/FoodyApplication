package foody.vn.Model;

public class UtilityModel {
    String utility_image;
    String utility_name;
    String utility_id;

    public String getUtility_id() {
        return utility_id;
    }

    public void setUtility_id(String utility_id) {
        this.utility_id = utility_id;
    }
    public String getUtility_image() {
        return utility_image;
    }

    public void setUtility_image(String utility_image) {
        this.utility_image = utility_image;
    }

    public String getUtility_name() {
        return utility_name;
    }

    public void setUtility_name(String utility_name) {
        this.utility_name = utility_name;
    }
}
